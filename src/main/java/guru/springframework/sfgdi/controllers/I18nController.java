package guru.springframework.sfgdi.controllers;

import guru.springframework.sfgdi.services.GreetingService;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.stereotype.Controller;

@Controller
public class I18nController {

    private final GreetingService greetingService;

    /*With the @Qualifier annotation we are indicating wich bean we want to inject
    * in this example we are going to try injecting 2 two different bean with the same name creating a conflict,
    * but using @Profile annotation we'll indicate which bean we want to inject*/
    public I18nController(@Qualifier("i18nService") GreetingService greetingService) {
        this.greetingService = greetingService;
    }

    public String sayHello(){
        return greetingService.sayGreeting();
    }
}
